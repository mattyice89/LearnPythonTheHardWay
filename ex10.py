# \t adds a tab in front of string
tabby_cat = "\tI'm tabbed in."
# \n puts the string on a new line
persian_cat = "I'm split\non a line."
# \\ just shows one slash, if you don't do two backslashes, then the
# program would think you're trying to enter a command
backslash_cat = "I'm \\ a \\ cat."

# """ just allows you to start typing and eliminates the need for
fat_cat = '''
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
'''

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
